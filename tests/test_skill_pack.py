    def test_agent_memory_summarizes_progress_events(self):
        script = SKILLS / "agent-memory" / "scripts" / "memory.py"
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            memory = root / "memories"
            progress = root / "progress"
